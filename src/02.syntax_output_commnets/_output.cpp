// =====================================
// 🎨 [출력 미션] 귀여운 토끼 얼굴을 그려보세요!
// 아래 그림을 참고하여 숫자/알파벳/특수문자를 조합해 줄 바꾸기와 출력 실습을 해보세요!
// 각 줄을 cout 으로 작성하며 줄바꿈은 \n 또는 endl을 활용하세요.
//
// 예시 출력:
//  (\_/)
//  (o.o)
//  (> <)
//  /   \
// =====================================

#include <iostream>   // 표준 입출력 라이브러리 (cout 사용 가능)
using namespace std;  // std:: 없이 cout, endl 등을 바로 사용하기 위해 선언

int main() {
    // 🐰 그림 출력 (줄바꿈 문자 \n 활용)
    cout << " (\\_/) \n";         // 백슬래시를 출력하려면 이스케이프 문자 \\ 사용
    cout << " (o.o) \n";
    cout << " (> <) \n";
    cout << " /   \\ \n";         // 마찬가지로 역슬래시는 \\로 출력

    // 👉 위와 동일한 결과를 endl을 사용하여 출력할 수도 있습니다.
    cout << "(\\_/)" << endl;
    cout << "(o.o)" << endl;
    cout << "(> <)" << endl;
    cout << "/   \\" << endl;

    // ✅ 문자열 여러 개 출력하기 (한 줄로 출력됨)
    cout << "안녕하세요! " << "C++ 출력 실습입니다!" << "\n";

    // ✅ 숫자 출력하기 (큰따옴표 없이 출력)
    cout << 123 << endl;
    cout << 3.14159 << "\n";

    // ✅ 수학 계산 결과 출력하기
    cout << "10 + 5 = " << 10 + 5 << endl;
    cout << "100 / 3 = " << 100 / 3 << "\n";  // 정수 나눗셈, 결과는 33

    // ✅ \n 여러 개 사용하여 빈 줄 만들기
    cout << "줄바꿈 실습 중입니다.\n\n다음 줄은 빈 줄 다음에 나옵니다!\n";

    // ✅ 이스케이프 문자 실습: \n, \t, \", \\
    cout << "이스케이프 문자 실습:\n";
    cout << "\t- 탭으로 들여쓰기 됨\n";
    cout << "\t- 큰따옴표 출력: \"Hello\"\n";
    cout << "\t- 역슬래시 출력: \\\n";

    // ✅ endl vs \n 비교
    cout << "endl은 줄을 바꾸고 출력 버퍼를 비웁니다." << endl;
    cout << "이 줄은 endl 다음 줄입니다.\n";
    cout << "이 줄은 \\n 다음 줄입니다." << endl;

    return 0;
}
