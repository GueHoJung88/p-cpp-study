/*
==============================================
🎮 C++ 주석 실습 미션 모음

1️⃣ 토끼 + 고양이 말풍선 완성하기
2️⃣ 주석으로 미로 탈출하기
3️⃣ 주석 처리로 미완성 테트리스를 완성하기

각 미션은 주석을 해제하거나 새로 추가하여,
출력을 완성해가는 방식입니다.

💡 모든 코드는 주석이 포함된 상태에서 실행하면
"불완전한 결과"가 출력됩니다.

🔧 주석을 조절하여 원하는 그림을 완성해보세요!
==============================================
*/

#include <iostream>
using namespace std;

int main() {

    /*
    ==========================================
    1️⃣ 토끼 + 고양이 말풍선 완성하기
    - 아래 주석을 적절히 해제하면 귀염뽀짝 토끼와 고양이 등장!
    ==========================================
    */

    cout << "\n\n";
    // 토끼 귀
    cout << "  (\\_/)" << endl;
    // cout << "  (•_•)" << endl;
    cout << "  ( -.-)" << endl;
    cout << "  />🍓" << endl;

    cout << endl;

    // 고양이 말풍선
    // cout << "      /\\_/\\    " << endl;
    // cout << "     ( o.o )   ~ 나도 이쁜 그림 보여줘냥" << endl;
    cout << "     ( >ω< )   ~ 당근 하나만 줘!" << endl;

    cout << "\n\n";


    /*
    ==========================================
    2️⃣ 미로 탈출 미션
    - 주석을 해제해서 미로 탈출 경로를 만드세요!
    ==========================================
    */

    cout << "미로 탈출 시작 ->" << endl;
    cout << "   ↓" << endl;
    // cout << " X 벽에 부딪혔다!" << endl;
    cout << "   ↓" << endl;
    cout << "   → 오른쪽으로 이동" << endl;
    // cout << "   ↓ 덫에 빠짐!" << endl;
    cout << "   → 탈출 성공! 🎉" << endl;

    cout << "\n\n";


    /*
    ==========================================
    3️⃣ 미완성 테트리스
    - 아래 블록 중 일부를 주석 처리하면 완성된 테트리스 블럭 모양 출력!
    - 조건: 빈 칸처럼 보이는 라인들을 주석 처리해보세요.
    ==========================================
    */

    cout << "⬛⬛⬛⬛⬛⬛⬛⬛⬛⬛" << endl;
    cout << "⬛      🟦🟦⬛⬛⬛⬛" << endl;
    cout << "⬛ 🟥🟥🟥  🟦⬛⬛⬛⬛" << endl;
    cout << "⬛ 🟩🟩  🟥🟥🟦⬛⬛⬛" << endl;
    // cout << "⬛        ⬛⬛⬛⬛⬛" << endl;
    cout << "⬛ 🟪🟪  🟩🟩🟥⬛⬛⬛" << endl;
    cout << "⬛ 🟨🟨🟪🟪🟩⬛⬛⬛⬛" << endl;
    cout << "⬛⬛⬛⬛⬛⬛⬛⬛⬛⬛" << endl;

    cout << "\n💡 주석을 활용해서 테트리스를 예쁘게 완성해보세요!\n";

    return 0;
}
